package Polynom;

public class Ele {
	private int cons;
	private int power;
	
	public Ele(int cons, int power) {
		this.cons = cons;
		this.power = power;
	}

	public int getCons() {
		return cons;
	}

	public void setCons(int cons) {
		this.cons = cons;
	}

	public int getPower() {
		return power;
	}

	public void setPower(int power) {
		this.power = power;
	}

}
